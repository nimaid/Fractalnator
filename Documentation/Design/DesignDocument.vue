        even when laying down. All locked objects have smoothed motion, so there is never sudden, 
        rigid movement of any locked geometry, regardless of user movement. The main feedback screen is soft locked, while various types of 
        geometry can be either soft or hard locked. Some experiences will allow for grabbing and positioning geometry in a 
        sandbox environment, where the player character can move around. Some experiences will only respond to controller movements to 
        influence pre-scripted geometry, and the objects locked to the player's view. The only motion allowed is head movements. The core experience will only respond to head movements, with pre-determined animations that can be influenced by audio files.</label>
