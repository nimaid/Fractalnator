            <label>Some objects can be "locked" to the user's field of view.
      
      
        There is "hard locking", where an object's position and rotation are 
        fully fixed to the camera view.
      
      
        There is "soft locking", where the object is positioned and rotated to 
        be within the player's field of view, but allows more freedom of 
        movement. The user is allowed to move slightly closer to or further 
        from the object. The object will not follow "tilting" of the head, 
        even when laying down.
      
      
        All locked objects have smoothed motion, so there is never sudden, 
        rigid movement of any locked geometry, regardless of user movement.
      
      
        The main feedback screen is soft locked, while various types of 
        geometry can be either soft or hard locked.
      
      
        Some experiences will allow for grabbing and positioning geometry in a 
        sandbox environment, where the player character can move around.
      
      
        Some experiences will only respond to controller movements to 
        influence pre-scripted geometry, and the objects locked to the 
        player's view. The only motion allowed is head movements.
      
      
